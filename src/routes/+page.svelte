<script lang="ts">
    import { goto } from "$app/navigation";

    function todayStr(): string {
        const n = new Date();
        const y = n.getFullYear();
        const m = String(n.getMonth() + 1).padStart(2, "0");
        const d = String(n.getDate()).padStart(2, "0");
        return `${y}-${m}-${d}`;
    }

    goto(`/day/${todayStr()}`, { replaceState: true });
</script>
